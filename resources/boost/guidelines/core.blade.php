## YouTube TestKit

- This application uses the YouTube TestKit (`viewtrender/php-youtube-testkit-laravel`) for mocking YouTube APIs in tests.
- Activate the `youtube-data-api` skill when testing videos, channels, playlists, search, comments, subscriptions, or captions.
- Activate the `youtube-analytics-api` skill when testing on-demand metrics queries, dashboards, or real-time stats.
- Activate the `youtube-reporting-api` skill when testing bulk data exports, background sync jobs, or CSV report pipelines.
